public class Third3 {
    public static void main(String[] args) {
        char grade='A';
        switch(grade){
            case 'A':
                System.out.println("Great");
                break;
            case 'B':
                System.out.println("Good");
                break;
            case 'C':
                System.out.println("OK");;
                break;
            default:
                System.out.println("Failed");
                break;
        }
    }
}
